CREATE TABLE IF NOT EXISTS AQDevices(
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDeviceID INT UNIQUE,
    Name VARCHAR(20),
    City VARCHAR(20),
    Country VARCHAR(20),
    Latitude FLOAT,
    Longitude FLOAT,
    Status VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS DataStream (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDeviceID INT,
    Pollutant VARCHAR(20),
    Value FLOAT,
    DateTime DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (AQDeviceID) REFERENCES AQDevices(AQDeviceID)
);

CREATE TABLE IF NOT EXISTS RawData (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDeviceID INT,
    PM25 FLOAT,
    PM10 FLOAT,
    O3 FLOAT,
    NO2 FLOAT,
    SO2 FLOAT,
    CO FLOAT,
    DateTime DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (AQDeviceID) REFERENCES AQDevices(AQDeviceID)
);

CREATE TABLE IF NOT EXISTS AQIResults (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDeviceID INT,
    PM25 FLOAT,
    PM10 FLOAT,
    O3 FLOAT,
    NO2 FLOAT,
    SO2 FLOAT,
    CO FLOAT,
    DateTime DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (AQDeviceID) REFERENCES AQDevices(AQDeviceID)
);